func Test_Hash224(t *testing.T) {
    var dst []byte

    h := New224()

    for _, tc := range testCases224 {
        msg, _ := hex.DecodeString(tc.Msg)

        h.Reset()
        h.Write(msg)
        dst = h.Sum(dst[:0])

        dString := hex.EncodeToString(dst)

        if dString != strings.ToLower(tc.MD) {
            t.Errorf("hash failed.\nresult: %s\nanswer: %s", dString, tc.MD)
            return
        }
    }
}

